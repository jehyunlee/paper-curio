import { marked } from "marked"
import { PaperMeta } from "../apis/zotero/item"
import { ReviewPayload } from "../llm/schema"
import { overallScore } from "./review-md"

function esc(s: string): string {
  return (s || "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
}

function md(s: string): string {
  try {
    return marked.parse(s || "", { async: false }) as string
  } catch {
    return esc(s)
  }
}

