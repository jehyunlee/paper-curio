export function renderIndexHtml(
  meta: PaperMeta,
  payload: ReviewPayload,
  opts: { provider: string; reviewDate: string },
): string {
  const overall = overallScore(payload)
  const authors = meta.authors.join(", ") || "(미상)"
  const urlHtml = meta.url
    ? `<a href="${esc(meta.url)}">${esc(meta.url)}</a>`
    : "(없음)"

  const badge = (label: string, v: number, cls = "") =>
    `<span class="badge ${cls}">${label} <b>${v.toFixed(1)}</b></span>`

  return `<!DOCTYPE html>
<html lang="ko">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${esc(meta.title)}</title>
<style>${CSS}</style>
</head>
<body>
<main>
  <h1>${esc(meta.title)}</h1>
  <div class="meta">${esc(authors)} &nbsp;|&nbsp; ${esc(meta.date || "(미상)")} &nbsp;|&nbsp; ${urlHtml}</div>

  <div class="essence">${md(payload.essence)}</div>

  <div class="scores">
    ${badge("Novelty", payload.novelty)}
    ${badge("Technical", payload.technical)}
    ${badge("Significance", payload.significance)}
    ${badge("Clarity", payload.clarity)}
    ${badge("Overall", overall, "overall")}
  </div>

  <h2>Motivation</h2>
  <ul>
    <li><b>Known</b>: ${md(payload.known)}</li>
    <li><b>Gap</b>: ${md(payload.gap)}</li>
    <li><b>Why</b>: ${md(payload.why)}</li>
    <li><b>Approach</b>: ${md(payload.approach)}</li>
  </ul>

  <h2>Achievement</h2>
  ${md(payload.achievement)}

  <h2>How</h2>
  ${md(payload.how)}

  <h2>Originality</h2>
  ${md(payload.originality)}

  <h2>Limitation</h2>
  ${md(payload.limitation)}

  <h2>Evaluation</h2>
  <blockquote>${esc(payload.verdict)}</blockquote>

  <div class="footer">
    Generated by Paper Curio · ${esc(opts.provider)} · ${esc(opts.reviewDate)} ·
    분류(category)는 paper-curation 빌드 시 자동 부여됩니다.
  </div>
</main>
</body>
</html>`
}
